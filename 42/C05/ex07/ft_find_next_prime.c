int		ft_find_next_prime(int nb)
{
	int x;

	x = 2;
	if (nb < 2)
		return (0);
	while (nb >= 2)
	{
		while (nb % x != 0)
			x++;
		if (nb == x)
			return (nb);
		else
		{
			nb++;
			x = 2;
		}
	}
	return (0);
}
